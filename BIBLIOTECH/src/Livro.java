public class Livro {
    private String titulo;
    private String autor;
    private String editora;
    private Integer anoPub;
    private Integer isbn;

    public Livro(String titulo, String autor, String editora, Integer anoPub, Integer isbn) {
        this.titulo = titulo;
        this.autor = autor;
        this.editora = editora;
        this.anoPub = anoPub;
        this.isbn = isbn;
    }
}

